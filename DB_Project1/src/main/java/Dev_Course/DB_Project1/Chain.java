package Dev_Course.DB_Project1;

public class Chain {
	
	private int id;
	private String name;
	
	public Chain(){};
	
	public Chain( String name) {		
		
		this.name = name;
	}	


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}
	
	
	
	
	
	
	
	

}
