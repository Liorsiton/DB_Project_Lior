package Dev_Course.DB_Project1;

public class MallGroup {
	
	private int id;
	private String name;
	
	public MallGroup(){};
	
	
	
	public MallGroup(int id, String name) {
		this.id = id;
		this.name = name;
	}



	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
	

}
